    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = new DefaultTableModel(new String[]{"ID", "Nama Depan", "Nama Belakang", "Jenis Kelamin", "Jenis Pengemudi"}, 0);
        JTable table = new JTable(model);

        // Menampung panel dan scroll pane
        JPanel panel = new JPanel();
        JScrollPane sp = new JScrollPane(table);

        panel.add(sp);
        jDialog1.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
        jDialog1.setContentPane(panel);
        jDialog1.pack();
        jDialog1.setVisible(true);

        // Mengambil data dari database dan mengisi model tabel
        try {
            if (conn == null || conn.isClosed()) {
                conn = ConnectToDatabase.getKoneksi();
            }
            Statement stm = conn.createStatement();
            ResultSet rs = stm.executeQuery("SELECT * FROM tb_ojekdaring");

            while (rs.next()) {
                int id = rs.getInt("no");
                String namaDepan = rs.getString("namadepan");
                String namaBelakang = rs.getString("namabelakang");
                String jenisKelamin = rs.getString("jeniskelamin");
                String jenisPengemudi = rs.getString("jeniskendaraan");
                model.addRow(new Object[]{id, namaDepan, namaBelakang, jenisKelamin, jenisPengemudi});
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

