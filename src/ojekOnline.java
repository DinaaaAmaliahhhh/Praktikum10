    private void daftarSekarangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_daftarSekarangActionPerformed
        if(lblCaptcha.getText().equals(inputCaptcha.getText())){
            if(persetujuan.isSelected()){
                if(!"Pilih Jenis Pengemudi".equals(jeniskendaraan.getSelectedItem().toString())){
                    try{
                        if(conn == null || conn.isClosed()){
                            conn = ConnectToDatabase.getKoneksi();
                        }

                        String query = "INSERT INTO tb_ojekdaring (namadepan, namabelakang, jeniskelamin, jeniskendaraan) values (?, ?, ?, ?)";

                        try(PreparedStatement ps = conn.prepareStatement(query)){
                            ps.setString(1, namaDepan.getText());
                            ps.setString(2, namaBelakang.getText());
                            ps.setString(3, pria.isSelected() ? "Pria" : "Wanita");
                            ps.setString(4, jeniskendaraan.getSelectedItem().toString());
                            ps.executeUpdate();
                        }
                        JOptionPane.showMessageDialog(null, "Pendaftaran Berhasil!");
                        resetForm();
                    }catch(SQLException ex){
                        JOptionPane.showMessageDialog(this, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
                    }
                }else{
                    JOptionPane.showMessageDialog(null, "Mohon pilih jenis pengemudi");
                }
            }else{
                JOptionPane.showMessageDialog(null, "Kolom persetujuan peraturan harus dicentang!");
            }
        }else{
            JOptionPane.showMessageDialog(null, "Captcha tidak sesuai!");
        } 
    }//GEN-LAST:event_daftarSekarangActionPerformed

