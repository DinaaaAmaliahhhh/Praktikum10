    static String generateCaptcha(int n){
        Random rand = new Random(62);
        String chrs ="abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789";
        
        String captcha = "";
        while(n-->0){
            int index = (int) (Math.random() * 62);
            captcha += chrs.charAt(index);
        }
        return captcha;
    }
    
